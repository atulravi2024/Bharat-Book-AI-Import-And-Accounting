const getAutoGeneratedCode = (
  type: "Staff" | "Customer" | "Vendor" | "Partner",
  data: any[],
  partyMasters: any[],
) => {
  if (type === "Staff") {
    const currentStaff = (data || []).filter(
      (m) =>
        m.contactType === "Internal" ||
        m.contactType === "Contract" ||
        m.contactType === "External" ||
        !m.contactType,
    );
    const count = currentStaff.length + 1;
    return `STF-${String(count).padStart(3, "0")}`;
  } else if (type === "Customer") {
    const currentCust = (partyMasters || []).filter(
      (m) => m.type === "Customer" || !m.type,
    );
    const count = currentCust.length + 1;
    return `CUST-${String(count).padStart(3, "0")}`;
  } else if (type === "Vendor") {
    const currentVend = (partyMasters || []).filter((m) => m.type === "Vendor");
    const count = currentVend.length + 1;
    return `VND-${String(count).padStart(3, "0")}`;
  } else if (type === "Partner") {
    const currentParts = (partyMasters || []).filter((m) => m.type === "Both");
    const count = currentParts.length + 1;
    return `PRT-${String(count).padStart(3, "0")}`;
  }
  return `CON-${Date.now()}`;
};

const getDeptAbbreviation = (dept: string): string => {
  switch (dept) {
    case "Management":
      return "MAN";
    case "Marketing & Ad":
      return "ADV";
    case "Finance":
      return "FIN";
    case "Publishing":
      return "PUB";
    case "Logistics & Supply":
      return "OPS";
    case "Customer Success":
      return "CS";
    case "Human Resources":
      return "HR";
    case "Sales & Distribution":
      return "SAL";
    case "Editorial & Content":
      return "EDT";
    case "Information Technology":
      return "SYS";
    case "Audit & Law":
      return "AUD";
    default:
      return dept ? dept.substring(0, 3).toUpperCase() : "";
  }
};

const generateStaffCode = (dept: string, allContacts: any[]) => {
  const abbrev = getDeptAbbreviation(dept);
  if (!abbrev) return "";

  const prefix = `STF-${abbrev}-`;
  let maxSeq = 0;
  (allContacts || []).forEach((c) => {
    if (c.code && c.code.startsWith(prefix)) {
      const suffix = c.code.replace(prefix, "");
      const num = parseInt(suffix, 10);
      if (!isNaN(num) && num > maxSeq) {
        maxSeq = num;
      }
    }
  });
  const nextSeq = maxSeq + 1;
  return `${prefix}${String(nextSeq).padStart(2, "0")}`;
};

